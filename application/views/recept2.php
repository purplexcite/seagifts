<h2>Креветки в кляре</h2>
<p>
Вначале необходимо приготовить жидкое тесто (кляр) из муки, яиц и молока.
<p>
Вареные очищенные креветки замариновать, для этого в посуду с растительным маслом добавить лимонной кислоты , а также перец соль и зелень петрушки. После на 15-20 минут поставить в холодильник.
<p>
Подготовленные таким образом креветки обмакнуть в тесто, а затем жарить в большом количестве жира. Через 4-5 минуты и вынуть и можно подавать к столу..
<p>
Ингридиенты для приготовления Креветки жареные в тесте:
<p>
вареных креветок - 800 г<br>
<ol>
<li>лимонной кислоты - 2-3 щепотки</li>
<li>растительного масла - 1 ст. ложка</li>
<li>и мелко нарезанная зелень петрушки</li>
</ol>
<p>
для теста:<br>
<ol>
<li>муки - 0.5 стакана</li>
<li>молока - 0.5 стакана</li>
<li>1 яйцо</li>
<li>жир для фритюра</li>
<li>молотый черный перец</li>
<li>соль по вкусу</li>
</ol>
<p>
Стоит заметить что чтобы кляр получился пышным и хрустящим в тесто можно добавить немного крахмала. Самый вкусный соус для креветок в кляре получается из элементарой сметаны и давленого чеснока с небольшим добавлением перца и соли. Но если вы хотите более оригинальный соус то можете использовать рецепт соуса для шавермы.
