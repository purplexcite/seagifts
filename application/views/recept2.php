<h2>Как варить тигровые креветки.</h2>
<p>
Основной способ приготовления креветок - варка. Очищать креветок от панциря можно до и после варки. Если их варят в панцире, в воду добавляют 2 столовые ложки на литр воды, если без панциря - 1 столовую ложку. На 1 часть креветок берут 3-4 части воды. Сырые креветки (размороженные на воздухе) при медленном кипении варят в подсоленной воде не более 3 минут, иначе их мясо станет очень жестким. Варено-мороженых креветок варят 5 минут. Креветки с проваренным мясом всплывают на поверхность. После варки креветок надо опустить на 2-3 минуты в холодную воду, тогда легко будет отделить мясо от панциря и удалить кишечник. Опять же время варки будет зависеть от размера самих креветок, обычно производители пишут на упаковке рекомендуемое время приготовления. Мясо приготовленных тигровых креветок становится белым, а черные полосы приобретают привлекательный ярко-красный цвет.
