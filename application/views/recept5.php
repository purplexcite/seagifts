<h2>Рецепт салата из осьминогов</h2>
<p>
Сегодня мы хотели бы поделиться с вами еще одним рецептом с осьминогом москардини. На этот раз это очень простой салат. Этот салат не только вкусный, но ещё и диетический, и очень полезный. Ведь морепродукты и оливковое масло действуют положительно на нашу сердечно-сосудистую систему. Также не менее важно употребление свежей зелени, в частности петрушки, и лимонного сока. Ведь эти продукты очень богаты витамином С. Осьминогов москардини, в отличие от других видов осьминогов, нужно варить примерно полчаса.
<p>
<b><u>Ингредиенты для салата из осьминогов москардини</u></b>
<p>
<ul>
<li>Осьминоги москардини: 2 шт. примерно 1 кг.</li>
<li>Оливковое масло: 100 гр.</li>
<li>Сок одного или двух лимонов.</li>
<li>Петрушка: несколько веточек.</li>
<li>Соль: по вкусу.</li>
</ul>
