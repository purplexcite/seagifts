<h2>Как приготовить креветки. Рецепты.</h2>
<p>
Если вы попали на эту статью то значит вы задались вопросом "как приготовить креветки". Нужно понимать что креветки - это блюдо очень полезное, так как состоит почти полностью из белка.  Самый распространенный способ приготовления креветок - просто сварить их. Скорее всего вы купили замороженные креветки в полиэтиленовом пакете. Такие креветки сами по себе УЖЕ вареные. Тоесть они нуждаются только во вторичной термической обработке (так же например как и сосиски, варя которые вы их не готовите а лишь разогреваете). Чтобы сварить креветки положите их в кипящую подсоленную воду и доведите её до кипения (после того как вы бросите в воду замороженных креветок она кипеть, разумеется, перестанет). Для вкуса можете бросить в воду лавровый лист, петрушку, репчатый лук, специи и тому подобное.
<p>
Пожарить креветки тоже несложно. Для этого раздавите несколько долек чеснока на сковородку с подсолнечным маслом и обжарьте в этом масле креветки. Вот как можно приготовить креветки самыми простыми способами.
<p>
Один из ключевых моментов  в приготовлении креветок - соус. Чесночный, сливочный или острый соус отлично подходят к нежному мясу креветок.
<p>
Так же креветки можно готовить как шашлык. Для этого подойдут лучше королевские креветки которые в несколько раз больше обычных.
<p>
<h2>Как правильно варить креветки.</h2>
<p>
Вообще-то варить креветки очень просто: залейте креветки кипящей водой, посолите и варите 5-10 минут после того как закипят. Даже при таком способе приготовления креветки получаются необычайно вкусними. Секрет наверно кроется в изначальной самодостаточности креветок :)
<p>
Если Вы хотите узнать оригинальный рецепт, вот он:
<p>
Расчитано на большую компанию, можно использовать на несколько партий креветок.
<p>
Кастрюлю около 5 литров, наполнить водой на 2/3.
<p>
В закипающую воду положить гвоздику (12-15 шт.), лавровый лист (4-6шт.), перец черный, горошком (около 12 горошин), душистый перец (6-8 шт.), половинку лимона, разрезанного поперек и вдоль, головку чеснока, разрезанного также поперек), 6-8 столовых ложек томатной пасты или порошок паприки (1-2 ст. ложки без горки), кому как нравится :), немного посолить.
<p>
Дать закипеть и в готовый "рассол" положить креветки.
<p>
Дать им повариться пару минут (после того, как закипят).
<p>
Вот и все! Приятного аппетита!
