<h2>Как пожарить креветки на костре </h2>
<p>
<b><u>Подготовка:</u></b> Инградиенты и маринад.<br>
.....Купите полкило или более свежих больших креветок, часто их называют тигровыми. Свежие (или мороженные) креветки должны быть серого цвета, красные - это уже вареные и вы не получите нужного эффекта. Далее возьмите один или несколько лимонов (один лимон на 0,5-0,7кг креветок). Выжмите сок лимона в ваши креветки, следует учесть, что посуда должна быть не металлической из-за разъедания лимонной кислотой. Насыпьте туда чайную ложку соли без бугра (на 0,5кг 1 ложка). Потом добавьте черного перца горошком и лаврового листа. В таком маринаде оставить креветки на четыре - шесть часов. Следует заранее подумать так же о шампурах - в идеале они должны быть тонкими (чуть толще спички). Можно использовать как альтернативу решетку для барбекю, в этом случае легко приготовить блюдо и на очень большую компанию, правда это будет менее экзотично.
<p>
<h2>креветки на гриле</h2>
<p>
<b><u>Приготовление:</u></b> жарка и подача.<br>
Разведите костер и дождитесь образования углей. Углей надо гораздо меньше, чем для приготовления шашлыков. Если будете использовать уже готовый уголь бросьте туда несколько веточек для получения ароматного дымка. Насадите креветки на шампура и расположите над углями. Уже через минуту они начнут краснеть и их необходимо будет переворачивать. Учтите, что в отличие от шашлыка они запекаются очень быстро и поэтому необходимо частое переворачивание.
<p>
Помните что свежие рецепты обновляются- на главной.<br>
Поливайте их сверху оставшимся маринадом. Используя решетку - перекладывайте еще не достаточно запеченые креветки к центру и то же поливайте маринадом. Максимум через 7-10 минут уникальное блюдо будет готово. Подавать рекомендуется с сухим белым вином, украсив их лимоном и укропом.
ПРИЯТНОГО АППЕТИТА !!! (не забывайте только снимать панцири у креветок :))
