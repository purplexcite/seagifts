<b><u>СПОСОБ ПРИГОТОВЛЕНИЯ</u></b>
<p>
Обжарить на раст.масле лук репч. добавить замороженный коктейль, посыпать приправы(я кладу обычно-орегано,базилик)немного соевого соуса.закрыть крышкой и тушить до готовности. Выложить на отварной рис.
